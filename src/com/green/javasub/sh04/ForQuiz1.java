package com.green.javasub.sh04;

public class ForQuiz1 {
    public static void main(String[] args) {
        int star = 5;
        for (int i = 0; i < star; i++) {
            System.out.print("*");
        }
        System.out.println();
        star = 10;
        for (int i = 0; i < star; i++) {
            System.out.print("*");
        }
        System.out.println();


    }
}
