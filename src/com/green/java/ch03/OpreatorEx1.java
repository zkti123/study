package com.green.java.ch03;

public class OpreatorEx1 {

    public static void main(String[] args) {

        int i = 5;

        i++;
        System.out.println(i);

        i = 5;
        ++i;
        ++i;
        System.out.println(i);

        i = 5;
        --i;
        System.out.println(i);




    }
}
