package com.green.java.ch03;

public class OpreatorStudy1 {

    public static void main(String[] args) {
        int num = 5;
        int result = 4+num*3;

        System.out.println(result);

        result = 11/ num;
        System.out.println(result);

        result = result-1;
        System.out.println(result);

        result = 13 % num;  //
        System.out.println(result); // %나머지값 구할때 사용 나머지 값이 1이나오면 무조건 홀수 0나오면 짝수




    }
}
